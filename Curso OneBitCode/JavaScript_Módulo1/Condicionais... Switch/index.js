const resultado = prompt("Digite uma letra minúscula entre 'a' e 'c'...");

switch (resultado) {
  case "a":
    console.log("O resultado é 'a'.");
    break;
  case "b":
    console.log("O resultado é 'b'.");
    break;
  case "c":
    console.log("O resultado é 'c'.");
    break;
  default:
    console.log("Finalizando...");
}
